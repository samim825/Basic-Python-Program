def sum(num1,num2):
    a=num1
    b=num2;
    return (a+b)
def sub(num1,num2):
    a=num1
    b=num2;
    return (a-b)
def mul(num1,num2):
    a=num1
    b=num2;
    return (a*b)
def div(num1,num2):
    a=num1
    b=num2;
    return (a/b)

print(sum(5,10))
print(sub(10,5))
print(mul(2,4))
print(div(12,4))
